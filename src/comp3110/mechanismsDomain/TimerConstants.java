package comp3110.mechanismsDomain;

/**
* The TimerConstants interface provides handy constants for specifying delays. Used as in
* 60*minutes+24*seconds
*/

public interface TimerConstants {
    
  public static final long milliseconds = 1;
  public static final long ms           = 1;
  public static final long seconds      = 1000;
  public static final long minutes      = 60000;
  public static final long hours        = 3600000;

}
